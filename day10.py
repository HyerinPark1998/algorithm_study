# 어차피 hp//5가 안나눠진다면 0이니까 나눈 몫과 그 나무지를 또 나눈 몫들을 더하면... 더하면... 완성...
# 그리고 나는 반복문을 while문만 생각했는데 아주 큰 오산이었다. 개미들을 리스트로 만들고 for문을 활용하는 방법도 있었다.
